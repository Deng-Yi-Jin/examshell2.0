#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	c;

	i = 1;
	if (argc != 2)
	{
		write(1, "z\n", 2);
		return (0);
	}
	while (i < argc)
	{
		if (argv[i][c] == 'z')
		{
			write(1, "z", 1);
			c++;
		}
		i++;
		write(1, "\n", 1);
	}
}
