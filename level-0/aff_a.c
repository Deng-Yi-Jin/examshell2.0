#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	c;

	i = 1;
	if (argc != 2)
		write(1, "a", 1);
	while (i < argc)
	{
		c = 0;
		if (argv[i][c] == 'a')
		{
			write(1, "a", 1);
			c++;
		}
		i++;
		write(1, "\n", 1); 
	}
}
