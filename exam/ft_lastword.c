#include <unistd.h>

int	main(int argc, char **argv)
{
	int i = 0;
	int j = 0;
	
	if (argc != 2)
		write(1, "\n", 1);
	while (argv[1][i])
	{
		if (argv[1][i - 1] == ' ' && ((argv[1][i] >= 'A' && argv[1][i] <= 'Z')
				|| (argv[1][i] >= 'a' && argv[1][i] <= 'z')))
		{
			j = i;
		}
		i++;
	}
	while (argv[1][j])
	{
		write(1, &argv[1][j], 1);
		j++;
	}
	write(1, "\n", 1);
	return (0);
}
